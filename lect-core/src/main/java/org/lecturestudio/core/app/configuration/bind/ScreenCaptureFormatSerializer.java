package org.lecturestudio.core.app.configuration.bind;

import com.fasterxml.jackson.core.JsonGenerator;
import com.fasterxml.jackson.databind.JsonSerializer;
import com.fasterxml.jackson.databind.SerializerProvider;
import org.lecturestudio.core.screencapture.ScreenCaptureFormat;

import java.io.IOException;

public class ScreenCaptureFormatSerializer extends JsonSerializer<ScreenCaptureFormat> {

    @Override
    public void serialize(ScreenCaptureFormat format, JsonGenerator generator, SerializerProvider provider) throws IOException {
        generator.writeStartObject();
        generator.writeNumberField("frameRate", format.getFrameRate());
    }
}
